    public static String convertToUnicode(String value) {
        if (value == null || value.length() == 0)
            return value;
        for (final String protype : PROTYP_TO_UNICODE_MAP.keySet())
            value = StringUtils.replace(value, protype, PROTYP_TO_UNICODE_MAP.get(protype));
        return value;
    }
